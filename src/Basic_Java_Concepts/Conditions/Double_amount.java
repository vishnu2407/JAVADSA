package Basic_Java_Concepts.Conditions;

public class Double_amount {

    public static void main(String[] args) {
        int amount = 1000;
        if(amount >= 1000){
            System.out.println("The amount is greater than or equal to 1000");
        }else{
            System.out.println("The amount is less than 1000");
        }
    }
}
